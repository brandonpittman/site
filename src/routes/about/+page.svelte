<p>
	I'm a frontend engineer and I live Nagoya, Japan but I'm originally from Cincinnati, Ohio. I first
	came to Japan in 2004 for study abroad and after returning to the US for one year to finish my
	degrees in International Relations and Asian Studies, I returned to Japan as an English teacher.
	After working in private and public schools for a decade, I became a self-taught programmer. I
	caught the programming bug while automating OmniFocus. After seeing what could be done with code, I
	learned Ruby and then Rails in hopes of turning my hobby into a career.
</p>

<p>
	After struggling to find enjoyment with the backend, I realized how much I enjoyed the frontend.
	While learning a bit of Laravel, I discovered <a href="https://vuejs.org">Vue.js</a> and quickly
	fell in love. I left my job as a teacher and began working in frontend development. Recently
	interested in functional programming, I got into
	<a href="https://reasonml.github.io">ReasonML</a> and then
	<a href="https://reasonml.github.io/reason-react/">ReasonReact</a>. With my newfound passion for
	functional programming, I found that React made more sense to me than Vue did, beginning my love
	affair with React. After that, it was <a href="https://qwik.dev">Qwik</a>, and now, it's
	<a href="https://svelte.dev">Svelte</a>.
</p>

<p>All my open-source work is up on <a href="https://github.com/brandonpittman">Github</a>.</p>

